//
// Literals
//

// Numeric Literals

an_int : 1;		print(an_int);
an_int8 : 1b;		print(an_int8);
an_int16 : 1s;		print(an_int16);
an_int32 : 1w;		print(an_int32);
an_int64 : 1l;		print(an_int64);
an_uint : 1u;		print(an_uint);
an_uint8 : 1ub;		print(an_uint8);
an_uint16 : 1us;	print(an_uint16);
an_uint32 : 1uw;	print(an_uint32);
an_uint64 : 1ul;	print(an_uint64);
a_float : 1.0;		print(a_float);
a_float32 : 1.0f;	print(a_float32);
a_float64 : 1.0d;	print(a_float64);

// Character Literals

a_character1 : 'a';	print(a_character1);
a_character2 : '\'';	print(a_character2);
a_character3 : '"';	print(a_character3);

// String Literals

a_string1 : "string";	print(a_string1);
a_string2 : "\"str\"";	print(a_string2);
